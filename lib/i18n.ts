export type Language = 'en' | 'pt'
export type TranslationKey = keyof typeof translations.en

export function getTranslation(lang: Language, key: string): any {
  const keys = key.split('.')
  let value: any = translations[lang]

  for (const k of keys) {
    value = value?.[k]
  }

  return value
}
